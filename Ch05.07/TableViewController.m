#import "TableViewController.h"
#import "RecentManager.h"
#import "TableViewCell.h"
#import "UITableViewCell+setInfo.h"
#import "HeightForRow.h"

@interface TableViewController ()

@property (strong, nonatomic)RecentManager *manager;
@property (strong, nonatomic)NSMutableArray *mArr;

@end

@implementation TableViewController

- (void)viewDidLoad {
    [super viewDidLoad];
    
    // Uncomment the following line to preserve selection between presentations.
    // self.clearsSelectionOnViewWillAppear = NO;
    
    // Uncomment the following line to display an Edit button in the navigation bar for this view controller.
    // self.navigationItem.rightBarButtonItem = self.editButtonItem;
    self.manager = [[RecentManager alloc]init];
    __block __weak TableViewController *copy_self = self;
    [self.manager getRecentSuccess:^(NSArray *arr) {
        copy_self.mArr = [NSMutableArray arrayWithArray:arr];
    }];
    //注册自定义cell
    [self.tableView registerClass:[TableViewCell class] forCellReuseIdentifier:@"cell"];
    
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (CGFloat)tableView:(UITableView *)tableView heightForRowAtIndexPath:(NSIndexPath *)indexPath{
    NSDictionary *dic = self.mArr[indexPath.row];
    return [HeightForRow heightForRowWithDic:dic];
}

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
#warning Potentially incomplete method implementation.
    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
#warning Incomplete method implementation.
    // Return the number of rows in the section.
    return self.mArr.count;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"cell" forIndexPath:indexPath];
    
    // Configure the cell...
    NSDictionary *dic = self.mArr[indexPath.row];
    [cell setInfo:dic];
    return cell;
}


